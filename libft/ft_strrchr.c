#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
		i++;
	while (i > -1 && s[i] != (char)c)
		i--;
	if (i == -1)
		return (NULL);
	return ((char *)s + i);
}
